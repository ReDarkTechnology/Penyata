	public static class EnumConverter
	{
		public static char[] numbers = "0123456789".ToCharArray();
		public static SDL.SDL_Keycode ToSDLKeyCode(this KeyCode v)
		{
			var s = v.ToString("D");
			return (SDL.SDL_Keycode)Enum.Parse(typeof(SDL.SDL_Keycode), s);
		}
		public static KeyCode ToKeyCode(this SDL.SDL_Keycode v)
		{
			var s = v.ToString("D");
			return (KeyCode)Enum.Parse(typeof(KeyCode), s);
		}
		public static uint ToUint(this MouseCode c)
		{
			return (uint)c;
		}
		public static SDL.SDL_WindowFlags ToSDLFlag(this WindowMode v)
		{
			return (SDL.SDL_WindowFlags)Enum.Parse(typeof(SDL.SDL_WindowFlags), v.ToString("D"));
		}
		public static SDL.SDL_RendererFlags ToSDLFlag(this RendererMode v)
		{
			return (SDL.SDL_RendererFlags)Enum.Parse(typeof(SDL.SDL_RendererFlags), v.ToString("D"));
		}
	}
